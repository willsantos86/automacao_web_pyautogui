import webbrowser

# Abrir uma aba no navegador existente ou criar outra
webbrowser.open('http://google.com')

# Abrir uma nova aba em um navegador existente ou criar outro
webbrowser.open_new_tab('https://www.facebook.com')

# Criar e abrir uma nova aba do zero
webbrowser.open_new('https://www.twitter.com')